namespace Pdf2xNet.Enums.Xpdf
{
    /// <summary>
    /// Represents the Xpdf tools that can be used for PDF conversion.
    /// </summary>
    internal enum XpdfTool
    {
        /// <summary>
        /// The PDF to HTML conversion tool.
        /// </summary>
        Pdf2Html,

        /// <summary>
        /// The PDF to PNG conversion tool.
        /// </summary>
        Pdf2Png,

        /// <summary>
        /// The PDF to plain text conversion tool.
        /// </summary>
        Pdf2Text
    }
}